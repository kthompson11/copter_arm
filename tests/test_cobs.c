static void test_stuff_empty_sequence(void **state)
{
    uint8_t sequence;
    uint8_t expected[] = {1, 0};
    uint8_t stuffed[10];
    int res = cobs_stuff(&sequence, stuffed, 0);

    assert_true(res == 2);
    assert_memory_equal(stuffed, expected, sizeof(expected) / sizeof(expected[0]));
}

static void test_stuff_short_sequence(void **state)
{
    uint8_t sequence[] = {1, 4, 0, 3, 2, 7, 0};
    uint8_t expected[] = {3, 1, 4, 4, 3, 2, 7, 1, 0};
    int len = sizeof(sequence) / sizeof(sequence[0]);
    uint8_t stuffed[10];

    int res = cobs_stuff(sequence, stuffed, len);

    assert_true(res == len + 2);
    assert_memory_equal(stuffed, expected, sizeof(expected) / sizeof(expected[0]));
}

